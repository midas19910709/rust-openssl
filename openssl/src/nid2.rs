use libc::c_int;

pub trait Nid {
    fn nid() -> c_int;
}

macro_rules! make_nids {
    ($($name:ident $id:expr,)+) => {
        $(
            pub struct $name;

            impl Nid for $name {
                fn nid() -> c_int {
                    $id
                }
            }
        )+
    }
}

